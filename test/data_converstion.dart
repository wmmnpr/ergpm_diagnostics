void readJsonFile(String filePath) {
  try {
    // Read the file
    final file = File(filePath);
    if (!file.existsSync()) {
      print('File not found: $filePath');
      return;
    }

    final contents = file.readAsStringSync();
    // Decode the JSON
    final List<dynamic> jsonList = jsonDecode(contents);

    Map<String, int>uniqueUuids = {};

    // Iterate through the list and print each object
    for (var item in jsonList) {
      var uuid = item["uuid128"].toString();
      if(!uniqueUuids.keys.contains(uuid)){
        uniqueUuids.putIfAbsent(uuid, () => 1);
      } else {
        int temp = uniqueUuids[uuid]?? 0;
        temp += 1;
        uniqueUuids[uuid] = temp;
      }
      var hexPayload = item["value"].toString().replaceAll(":", "");
      List<int> buffer = HEX.decode(hexPayload);
      if (uuid.startsWith("ce:06:00:33:")) {
        print(uuid);
      } else if (uuid.startsWith("ce:06:00:35:")) {
        StrokeData strokeData = intListToStrokeData(buffer);
        var jsonStrokeData = json.encode(strokeData.toJson());
        print(jsonStrokeData);
      } else if (uuid.startsWith("ce:06:00:36:")) {
        print(uuid);
      }
    }

    print(uniqueUuids);
  } catch (e) {
    print('Error reading JSON file: $e');
  }
}

